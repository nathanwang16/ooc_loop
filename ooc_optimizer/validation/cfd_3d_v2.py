"""
Module 4.1 — 3D CFD validation (v2).

For a BO winner produced by Module 3.1 we re-run both solvers at higher
fidelity (3D) on the same parametric geometry used during 2D BO.  The key
number the manuscript quotes is

    ΔL² = L2_to_target(3D) − L2_to_target(2D)

If ΔL² ≲ 10% in relative terms the 2D-during-BO approximation is validated;
if it is larger, the BO of that target profile would need to be re-run in 3D
(Development Guide v2 §8).  WSS diagnostics are retained as a flow-field
sanity check (matching the v1 2D-vs-3D comparison used in the premise test).

Geometry
--------
The 3D blockMesh is generated by
``ooc_optimizer.geometry.generate_blockmesh_dict_v2_3d`` with configurable
``nz`` (number of z-layers) and ``z_grading`` (end/start ratio; < 1
concentrates cells near the floor to resolve the floor WSS boundary layer).
``snappyHexMesh`` with a 5-layer boundary layer addition can replace the
blockMesh path via ``use_snappy=True``; the fallback blockMesh keeps the
validation self-contained when no STL is available.

This module is intentionally separate from :mod:`ooc_optimizer.validation.cfd_3d`
(which still serves the v1 WSS premise test) to keep each script single-
purpose and to decouple the v1 retention path from v2 feature work.
"""

