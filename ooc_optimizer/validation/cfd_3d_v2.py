logger = logging.getLogger(__name__)

SOLVER_TIMEOUT_S = 1800  # 30 min is the per-stage ceiling for 3D


@dataclass
class Result3D:
    """Summary of one 3D validation run."""

    case_dir: Path
    winner_spec: Dict[str, Any]
    L2_to_target_3d: float
    L2_to_target_2d: Optional[float]
    L2_relative_delta: Optional[float]
    tau_floor_mean_3d: float
    tau_floor_max_3d: float
    tau_floor_cv_3d: float
    wall_time_s: float
    converged_U: bool
    converged_C: bool
    warnings: List[str] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "case_dir": str(self.case_dir),
            "winner_spec": dict(self.winner_spec),
            "L2_to_target_3d": float(self.L2_to_target_3d),
            "L2_to_target_2d": None if self.L2_to_target_2d is None else float(self.L2_to_target_2d),
            "L2_relative_delta": (
                None if self.L2_relative_delta is None else float(self.L2_relative_delta)
            ),
            "tau_floor_mean_3d": float(self.tau_floor_mean_3d),
            "tau_floor_max_3d": float(self.tau_floor_max_3d),
            "tau_floor_cv_3d": float(self.tau_floor_cv_3d),
            "wall_time_s": float(self.wall_time_s),
            "converged_U": bool(self.converged_U),
            "converged_C": bool(self.converged_C),
            "warnings": list(self.warnings),
        }


