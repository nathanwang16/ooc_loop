def _compute_l2_floor_3d(
    case_dir: Path, target_profile: TargetProfile, L: float, W: float
) -> float:
    """Compute L2_to_target on the floor (z ≈ 0 layer) for the 3D case."""
    time_dir = find_latest_time(case_dir)
    if time_dir is None:
        return float("nan")
    t_file = time_dir / "T"
    if not t_file.exists():
        return float("nan")
    centres = read_cell_centres(case_dir)
    C = read_scalar_field(t_file)
    if C.shape[0] != centres.shape[0]:
        return float("nan")
    # Select cells in the lowest z-decile (cell-centre z in [0, H/10]).
    z = centres[:, 2]
    z_min = float(z.min())
    z_max = float(z.max())
    z_thresh = z_min + 0.1 * max(z_max - z_min, 1e-12)
    mask = z <= z_thresh
    if mask.sum() < 10:
        mask = z <= (z_min + 0.3 * (z_max - z_min))
    C_floor = C[mask]
    centres_floor = centres[mask]
    C_target = target_profile.evaluate(centres_floor[:, 0], centres_floor[:, 1], L=L, W=W)
    return float(l2_to_target(C_floor, C_target))


def _floor_wss_statistics(case_dir: Path, mu: float) -> tuple[float, float, float]:
    """Return (mean, max, CV) of ‖τ_wall‖ on the floor patch.

    Falls back to a U-gradient estimate (``τ ≈ μ · |u_t|/z``) at the first
    cell-centre above the floor if the ``wallShearStress`` function object
    was not written.
    """
    time_dir = find_latest_time(case_dir)
    if time_dir is None:
        return float("nan"), float("nan"), float("nan")

    wss_path = time_dir / "wallShearStress"
    if wss_path.exists():
        magnitudes = _parse_floor_wss(wss_path)
        if magnitudes.size > 0:
            # OpenFOAM wallShearStress units are m^2/s^2 = τ/ρ; convert to Pa
            # assuming ρ = 1000 (water/media).  This is consistent with v1.
            tau_Pa = magnitudes * 1000.0
            return (
                float(tau_Pa.mean()),
                float(tau_Pa.max()),
                float(tau_Pa.std() / max(tau_Pa.mean(), 1e-12)),
            )

    # Fallback: first-cell-above-floor tangential velocity gradient.
    U = read_vector_field(time_dir / "U")
    centres = read_cell_centres(case_dir)
    z = centres[:, 2]
    z_min = float(z.min())
    z_thresh = z_min + 0.05 * (float(z.max()) - z_min)
    mask = z <= z_thresh
    if mask.sum() < 10:
        return float("nan"), float("nan"), float("nan")
    u_tan = np.linalg.norm(U[mask, :2], axis=1)
    z_floor = z[mask] - z_min + 1e-9
    tau = mu * u_tan / z_floor
    return float(tau.mean()), float(tau.max()), float(tau.std() / max(tau.mean(), 1e-12))


def _parse_floor_wss(wss_path: Path) -> np.ndarray:
    text = wss_path.read_text()
    patch_match = re.search(r"floor\s*\{(.*?)\n\s*\}", text, flags=re.DOTALL)
    if patch_match is None:
        return np.array([])
    non = re.search(
        r"value\s+nonuniform\s+List<vector>\s+\d+\s*\((.*?)\)\s*;",
        patch_match.group(1),
        flags=re.DOTALL,
    )
    if non is None:
        return np.array([])
    trip = re.findall(r"\(\s*([-+\d.eE]+)\s+([-+\d.eE]+)\s+([-+\d.eE]+)\s*\)", non.group(1))
    if not trip:
        return np.array([])
    vec = np.array([[float(a), float(b), float(c)] for a, b, c in trip])
    return np.linalg.norm(vec, axis=1)


def dump_results(results: List[Result3D], output_path: Path) -> Path:
    """Write a JSON summary of multiple 3D validation runs."""
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    payload = [r.to_dict() for r in results]
    with open(output_path, "w") as f:
        json.dump(payload, f, indent=2)
    return output_path
