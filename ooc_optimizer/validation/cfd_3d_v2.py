def validate_winner_3d(
    *,
    winner: Dict[str, Any],
    config: Dict[str, Any],
    target_profile_spec: Dict[str, Any],
    output_dir: Path,
    nz: int = 25,
    z_grading: float = 1.0,
    base_nx: int = 300,
    ny_per_mm: int = 30,
    diffusivity_m2_s: Optional[float] = None,
) -> Result3D:
    """Run a full 3D simpleFoam + scalarTransportFoam validation for one winner.

    Parameters
    ----------
    winner : dict
        The ``best_feasible`` record produced by Module 3.1 (or a dict with
        ``params``, ``topology``, ``pillar_config``, ``H``).
    config : dict
        Loaded YAML config (needs ``fixed_parameters`` and ``paths``).
    target_profile_spec : dict
        Target profile spec (same keys as the YAML ``target_profile`` block).
    output_dir : Path
        Parent directory for the 3D case; one subdirectory is created per run.
    nz : int
        Number of z-cells for the 3D mesh.
    z_grading : float
        blockMesh simpleGrading z end/start ratio (< 1 → cells near floor).
    base_nx, ny_per_mm : int
        Lateral mesh resolution.
    diffusivity_m2_s : float, optional
        Override the config's default DT; required if the v1 config has no
        ``diffusivity`` key.
    """
    params = dict(winner["params"])
    topology = winner.get("topology", winner.get("inlet_topology"))
    if topology is None:
        raise ValueError("winner must carry 'topology' or 'inlet_topology'")
    pillar_config = winner.get("pillar_config", "none")
    H_um = float(winner.get("H", 200.0))
    if pillar_config != "none":
        # For 3D validation in this module we support blockMesh-only; pillar
        # cases would need snappyHexMesh which is handled by the CFD
        # orchestrator in module 2.2 (not repeated here to keep scope tight).
        logger.warning(
            "3D validation with pillar_config=%s is not supported via blockMesh; "
            "the winning geometry is meshed without pillars.", pillar_config,
        )

    target_profile = build_target_profile(dict(target_profile_spec))
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    case_name = f"3d_{topology}_H{int(H_um)}_{int(time.time()*1000)}"
    case_dir = output_dir / case_name

    mu = float(config["fixed_parameters"]["fluid_viscosity_Pa_s"])
    rho = float(config["fixed_parameters"].get("fluid_density_kg_m3", 1000.0))
    if diffusivity_m2_s is None:
        diffusivity_m2_s = float(config.get("diffusivity", DEFAULT_DIFFUSIVITY_M2_S))

    # --- stage case ------------------------------------------------------
    template_dir = Path(config["paths"]["template_case"])
    if case_dir.exists():
        shutil.rmtree(case_dir)
    shutil.copytree(template_dir, case_dir)

    bm = generate_blockmesh_dict_v2_3d(
        params=params,
        topology=topology,
        H_um=H_um,
        base_nx=base_nx,
        ny_per_mm=ny_per_mm,
        nz=nz,
        z_grading=z_grading,
    )
    (case_dir / "system" / "blockMeshDict").write_text(bm.content)

    Q_total_m3s = float(params["Q_total"]) * 1e-9 / 60.0
    r_flow = float(params["r_flow"])
    U_drug = (r_flow * Q_total_m3s) / max(bm.inlet_drug_area_m2, 1e-20)
    U_medium = ((1.0 - r_flow) * Q_total_m3s) / max(bm.inlet_medium_area_m2, 1e-20)
    drug_vec = (0.0, U_drug, 0.0) if topology == "asymmetric_lumen" else (U_drug, 0.0, 0.0)
    medium_vec = (U_medium, 0.0, 0.0)

    _write_u_field_3d(case_dir / "0" / "U", drug_vec, medium_vec)
    _write_p_field_3d(case_dir / "0" / "p")
    _write_t_field_3d(case_dir / "0" / "T")

    # --- momentum solve --------------------------------------------------
    start = time.perf_counter()
    if not _run_blockmesh_3d(case_dir):
        raise RuntimeError(f"blockMesh failed for 3D case {case_dir}")
    converged_U = _run_simplefoam_3d(case_dir)
    if not converged_U:
        logger.warning("3D simpleFoam failed to meet residuals for %s", case_dir.name)

    # --- scalar solve ----------------------------------------------------
    # Add wallShearStress function object + cell centres before the scalar
    # overwrites controlDict.
    _run_postprocess(case_dir, "writeCellCentres")
    wall_shear_ok = _run_postprocess(case_dir, "wallShearStress", ignore_errors=True)
    scalar_result = run_scalar_transport(
        case_dir=case_dir,
        diffusivity_m2_s=diffusivity_m2_s,
        c_drug=1.0,
        c_medium=0.0,
        patches={
            "inlet_drug": "fixedValue:1",
            "inlet_medium": "fixedValue:0",
            "outlet": "zeroGradient",
            "walls": "zeroGradient",
            "floor": "zeroGradient",
            "ceiling": "zeroGradient",
        },
    )
    wall_time_s = time.perf_counter() - start

    # --- metrics ---------------------------------------------------------
    L2_3d = _compute_l2_floor_3d(case_dir, target_profile, bm.chamber_length_m, bm.chamber_width_m)
    tau_mean, tau_max, tau_cv = _floor_wss_statistics(case_dir, mu)

    L2_2d = None
    if "L2_to_target" in (winner.get("metrics") or {}):
        L2_2d = float(winner["metrics"]["L2_to_target"])
    elif "L2_to_target" in winner:
        L2_2d = float(winner["L2_to_target"])
    rel_delta = None
    if L2_2d is not None and L2_2d > 1e-12:
        rel_delta = (L2_3d - L2_2d) / L2_2d

    return Result3D(
        case_dir=case_dir,
        winner_spec={
            "params": params,
            "topology": topology,
            "pillar_config": pillar_config,
            "H": H_um,
            "target_profile": target_profile_spec,
        },
        L2_to_target_3d=float(L2_3d),
        L2_to_target_2d=L2_2d,
        L2_relative_delta=rel_delta,
        tau_floor_mean_3d=tau_mean,
        tau_floor_max_3d=tau_max,
        tau_floor_cv_3d=tau_cv,
        wall_time_s=wall_time_s,
        converged_U=bool(converged_U),
        converged_C=bool(scalar_result.converged),
        warnings=list(scalar_result.warnings) + ([] if wall_shear_ok else ["wallShearStress unavailable"]),
    )


def validate_all_winners(
    *,
    orchestrator_results: Dict[str, Any],
    config: Dict[str, Any],
    output_dir: Path,
    **kwargs: Any,
) -> List[Result3D]:
    """Run ``validate_winner_3d`` on every winner from a multi-target sweep.

    Expects the dict returned by
    ``ooc_optimizer.optimization.run_multi_target_workflow``: one primary
    winner plus zero or more secondary winners.  Each gets its own 3D case.
    """
    all_winners: List[Dict[str, Any]] = []
    primary = orchestrator_results.get("primary") or {}
    pw = primary.get("winner")
    if pw is not None:
        pw = dict(pw)
        pw["target_profile"] = pw.get("target_profile") or orchestrator_results.get("primary_target_profile")
        all_winners.append(pw)
    for sec in orchestrator_results.get("secondary", []) or []:
        sw = sec.get("winner")
        if sw is not None:
            sw = dict(sw)
            sw["target_profile"] = sec.get("target_profile")
            all_winners.append(sw)

    out: List[Result3D] = []
    for w in all_winners:
        spec = w.get("target_profile") or {"kind": "linear_gradient"}
        logger.info("3D validation for winner %s (target=%s)", w.get("config_name"), spec)
        res = validate_winner_3d(
            winner=w,
            config=config,
            target_profile_spec=spec,
            output_dir=Path(output_dir),
            **kwargs,
        )
        out.append(res)
    return out


