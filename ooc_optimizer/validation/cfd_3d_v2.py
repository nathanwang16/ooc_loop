def _run_blockmesh_3d(case_dir: Path) -> bool:
    prefix = _find_openfoam_prefix()
    cmd = ["blockMesh"] if not prefix else [prefix, "-c", "blockMesh"]
    try:
        result = subprocess.run(
            cmd, cwd=case_dir, capture_output=True, text=True, check=True, env=os.environ
        )
        (case_dir / "blockMesh.log").write_text(result.stdout + "\n" + result.stderr)
        return True
    except subprocess.CalledProcessError as exc:
        (case_dir / "blockMesh.log").write_text((exc.stdout or "") + "\n" + (exc.stderr or ""))
        return False


def _run_simplefoam_3d(case_dir: Path, timeout: int = SOLVER_TIMEOUT_S) -> bool:
    log_path = case_dir / "simpleFoam.log"
    prefix = _find_openfoam_prefix()
    cmd = ["simpleFoam"] if not prefix else [prefix, "-c", "simpleFoam"]
    try:
        with open(log_path, "w") as log_file:
            subprocess.run(
                cmd, cwd=case_dir, stdout=log_file, stderr=subprocess.STDOUT,
                timeout=timeout, check=True, env=os.environ,
            )
        return _check_residuals(log_path)
    except (subprocess.CalledProcessError, subprocess.TimeoutExpired):
        return False


def _run_postprocess(case_dir: Path, func: str, *, ignore_errors: bool = False) -> bool:
    prefix = _find_openfoam_prefix()
    cmd = ["postProcess", "-func", func, "-latestTime"]
    if prefix:
        cmd = [prefix, "-c", " ".join(cmd)]
    try:
        subprocess.run(cmd, cwd=case_dir, check=True, capture_output=True, env=os.environ)
        return True
    except subprocess.CalledProcessError as exc:
        if not ignore_errors:
            raise
        (case_dir / f"postProcess_{func}.log").write_text(
            (exc.stdout or b"").decode("utf-8", errors="replace")
            + "\n" + (exc.stderr or b"").decode("utf-8", errors="replace")
        )
        return False


def _check_residuals(log_path: Path, threshold: float = 1e-4) -> bool:
    if not log_path.exists():
        return False
    pattern = re.compile(r"final residual\s*=\s*([0-9eE\+\-\.]+)", re.IGNORECASE)
    converged_u, converged_p = False, False
    for line in reversed(log_path.read_text().splitlines()):
        if any(v in line for v in ("Solving for Ux", "Solving for Uy", "Solving for Uz")):
            m = pattern.search(line)
            if m and float(m.group(1)) < threshold:
                converged_u = True
        if "Solving for p" in line:
            m = pattern.search(line)
            if m and float(m.group(1)) < threshold:
                converged_p = True
        if converged_u and converged_p:
            return True
    return False


